package Statements;
import java.util.*;
public class Switch {
    public static void main(String[] args) {
        int number = 2;
        switch (number) {
            case 1:System.out.println("Jatin");
                
                break;

                case 2:System.out.println("Nitin");
                
                break;

                case 3:System.out.println("Nikhil");
                
                break;
        
            default:
                break;
        }
    }
}
